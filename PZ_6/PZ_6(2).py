#Дан целочисленный список размера N, содержащий ровно два одинаковых элемента.
#Найти номера одинаковых элементов и вывести эти номера в порядке возрастания.

def possitive(arr):
    # Создаем словарь для хранения индексов каждого элемента
    indecs = {}

    for i, num in enumerate(arr):
        if num in indecs:
            # Если элемент уже встречался, возвращаем его индексы
            return sorted([indecs[num], i])
        else:
            # Если элемент встречается впервые, запоминаем его индекс
            indecs[num] = i

    return None  # Если одинаковых элементов не найдено

# Пример использования
try:
    N = int(input("Введите размер списка: "))
    if N <= 0:
        print("Ошибка: Размер списка должен быть положительным целым числом.")
    else:
        arr = []
        for i in range(N):
            value = int(input(f"Введите элемент {i + 1}: "))
            arr.append(value)

        result = possitive(arr)
        if result:
            print("Индексы одинаковых элементов:", result)
        else:
            print("Одинаковых элементов не найдено.")
except ValueError:
    print("Ошибка: Пожалуйста, введите целое число.")