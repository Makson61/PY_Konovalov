#Даны положительные числа А и В (А > Б). На отрезке длины А размещено максимально возможное количество отрезков длины В (без наложений).
# Не используя операции умножения и деления, найти количество отрезков Б, размещенных на отрезке А.

try:
    A = int(input("Введите длину отрезка A: "))
    B = int(input("Введите длину отрезка B: "))

    if A <= 0 or B <= 0 or A < B:
        print("Ошибка: A и B должны быть положительными числами, и A должно быть больше B.")
    else:
        count = 0
        remaining = A
        while remaining >= B:
            remaining -= B
            count += 1
        print("Количество отрезков B:", count)

except ValueError:
    print("Ошибка: Введите целые числа.")