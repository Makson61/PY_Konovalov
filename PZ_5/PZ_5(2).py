#Описать функцию Powerl(A, В) вещественного типа, находящую величину АВ по формуле АВ = exp(B*In(A))
#(параметры А и В - вещественные). В случае нулевого или отрицательного параметра А функция возвращает 0. С помощью этой функции найти степени А^P, В^P, С^P, если даны числа P, A, B, C

def Powerl(A, B):
    """Функция, вычисляющая A^B. Возвращает 0, если A <= 0."""
    if A <= 0:
        return 0  # Если A ноль или отрицательное, возвращает 0
    return A ** B  # Вычисление A^B с помощью оператора **

try:
    P = float(input("Введите значение P (степень): "))
    A = float(input("Введите значение A: "))
    B = float(input("Введите значение B: "))
    C = float(input("Введите значение C: "))

    #функция для вычисления степеней
    A_power = Powerl(A, P)
    B_power = Powerl(B, P)
    C_power = Powerl(C, P)

    print(f"A^{P} = {A_power}")
    print(f"B^{P} = {B_power}")
    print(f"C^{P} = {C_power}")

except ValueError:
    print("Ошибка: Пожалуйста, введите допустимые числовые значения.")