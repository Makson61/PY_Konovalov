#Найти сумму чисел ряда 1,2,3,4,... от числа n до числа m.
# Суммирование оформить функцией с параметрами. Значения n и m программа должна запрашивать.

def possitive (n, m):
    """Функция для вычисления суммы чисел от n до m."""
    if n > m:
        return 0
    return sum(range(n, m + 1))  # Вычисляем сумму чисел от n до m (включительно)


try:
    n = int(input("Введите начальное число (n): "))
    m = int(input("Введите конечное число (m): "))

    # Вычисляем сумму
    result = possitive(n, m)

    # Выводим результат
    print(f"Сумма чисел от {n} до {m} равна {result}.")

except ValueError:
    print("Ошибка: Пожалуйста, введите допустимые целые числа.")